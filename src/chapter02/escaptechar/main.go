package main

import "fmt"

//转义字符
func main(){
	// \t
	fmt.Println("Tom\tjack")

	// \n
	fmt.Println("hello\nworld")

	// \\
	fmt.Println("c:\\user")
	// \"
	fmt.Println("tom say \"i love you\"")

	// \r 回车
	fmt.Println("天龙八部关羽\r张飞")


	fmt.Println("姓名\t年龄\t籍贯\t住址\njoho\t12\t河北\t北京")

	fmt.Println("姓名：ahao\n年龄：23\n籍贯：河南\n住址：上海")


	
	 
}